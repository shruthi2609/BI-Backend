const express=require('express')
const server=express()
const bp=require("body-parser")
server.use(bp.json())
const register=require('./routes/authentication/signUp')
const signin=require('./routes/authentication/signin')
const confidential=require('./routes/authentication/ProtectedRoute')
server.use('/',register)
server.use('/',signin)
server.use('/',confidential)

server.listen(3001,()=>console.log('server started'))